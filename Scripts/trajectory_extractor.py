#!/usr/bin/env python3
"""
trajectory_extractor.py

Point it at the Results folder generated by traj_macro.py.
It walks every pair subfolder, pairs each background-corrected stack
with its own results CSV, and extracts trajectories for client and chap.

Expected structure (generated by traj_macro.py):
    Results/
        client_1/
            client_1_background_corrected.tif
            chap_1_background_corrected.tif
            client_1_results.csv
            chap_1_results.csv
            Client_trajectories/
            Chap_trajectories/
        client_2/
            ...

Output trajectories are saved into the existing
Client_trajectories/ and Chap_trajectories/ subfolders.
Rows = frames, columns = Mean_0, Mean_1, … (one column per peak)
"""

from pathlib import Path
import numpy as np
import pandas as pd
import tifffile

# ─────────────────────────────────────────────────────────────────────────────
# CONFIG
# ─────────────────────────────────────────────────────────────────────────────

RESULTS_FOLDER    = Path(r"C:\path\to\Exp\Results")

TRAJ_INNER_RADIUS = 2   # inner box radius → 5×5 px
TRAJ_OUTER_RADIUS = 4   # outer box radius → 9×9 px


# ─────────────────────────────────────────────────────────────────────────────
# Core functions
# ─────────────────────────────────────────────────────────────────────────────

def load_stack(path: Path) -> np.ndarray:
    # Page-by-page avoids broken OME metadata parsing in tifffile
    with tifffile.TiffFile(str(path)) as tif:
        arr = np.stack([p.asarray() for p in tif.pages])
    print(f"      {path.name}: {arr.shape[0]} frames, {arr.shape[1]}×{arr.shape[2]} px")
    return arr


def measure_trajectories(stack: np.ndarray, coords: pd.DataFrame) -> pd.DataFrame:
    n_frames, H, W = stack.shape
    ri, ro = TRAJ_INNER_RADIUS, TRAJ_OUTER_RADIUS
    n_peaks = len(coords)

    if n_peaks == 0:
        return pd.DataFrame()

    data = np.zeros((n_frames, n_peaks))

    for i, row in coords.iterrows():
        cx, cy = int(round(row['X'])), int(round(row['Y']))
        for f in range(n_frames):
            img = stack[f].astype(float)
            yi = slice(max(0, cy - ri), min(H, cy + ri + 1))
            xi = slice(max(0, cx - ri), min(W, cx + ri + 1))
            yo = slice(max(0, cy - ro), min(H, cy + ro + 1))
            xo = slice(max(0, cx - ro), min(W, cx + ro + 1))
            data[f, i] = img[yi, xi].mean() - img[yo, xo].mean()

    return pd.DataFrame(data, columns=[f'Mean_{i}' for i in range(n_peaks)])


def find_pair_files(pair_dir: Path) -> tuple:
    """
    Returns (client_stack, client_csv, chap_stack, chap_csv) paths from a pair folder.
    Matches files by stem: anything containing 'client' or 'chap'.
    """
    stacks = {p.stem.replace('_background_corrected', ''): p
              for p in pair_dir.glob('*_background_corrected.tif')}
    csvs   = {p.stem.replace('_results', ''): p
              for p in pair_dir.glob('*_results.csv')}

    client_key = next((k for k in stacks if 'client' in k), None)
    chap_key   = next((k for k in stacks if 'chap'   in k), None)

    client_stack = stacks.get(client_key)
    chap_stack   = stacks.get(chap_key)
    client_csv   = csvs.get(client_key)
    chap_csv     = csvs.get(chap_key)

    return client_stack, client_csv, chap_stack, chap_csv


# ─────────────────────────────────────────────────────────────────────────────
# Main
# ─────────────────────────────────────────────────────────────────────────────

def main() -> None:
    pair_dirs = sorted([d for d in RESULTS_FOLDER.iterdir() if d.is_dir()])

    if not pair_dirs:
        print(f"No subfolders found in {RESULTS_FOLDER}")
        return

    print(f"Found {len(pair_dirs)} pair folder(s) in {RESULTS_FOLDER}\n")

    for pair_dir in pair_dirs:
        print(f"{'─'*55}")
        print(f"  {pair_dir.name}")

        client_stack, client_csv, chap_stack, chap_csv = find_pair_files(pair_dir)

        # ── Client ────────────────────────────────────────────────────────
        if client_stack and client_csv:
            print(f"  [Client]")
            stack  = load_stack(client_stack)
            coords = pd.read_csv(client_csv)
            print(f"      {len(coords)} peaks")
            traj   = measure_trajectories(stack, coords)
            out    = pair_dir / "Client_trajectories" / f"{client_stack.stem}_trajectories.csv"
            out.parent.mkdir(exist_ok=True)
            traj.to_csv(out, index=False)
            print(f"      Saved → {out.name}")
        else:
            print(f"  [Client] Missing stack or CSV — skipping.")

        # ── Chap ──────────────────────────────────────────────────────────
        if chap_stack and chap_csv:
            print(f"  [Chap]")
            stack  = load_stack(chap_stack)
            coords = pd.read_csv(chap_csv)
            print(f"      {len(coords)} peaks")
            traj   = measure_trajectories(stack, coords)
            out    = pair_dir / "Chap_trajectories" / f"{chap_stack.stem}_trajectories.csv"
            out.parent.mkdir(exist_ok=True)
            traj.to_csv(out, index=False)
            print(f"      Saved → {out.name}")
        else:
            print(f"  [Chap] Missing stack or CSV — skipping.")

    print(f"\n{'═'*55}")
    print("Done.")


if __name__ == "__main__":
    main()
